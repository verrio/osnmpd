static void test_public_pdu(const uint8_t *ref_pdu, size_t ref_pdu_len, int offset, size_t len, int scoped_offset, char *name)
{
	asn1raw_t src;
	src.value = (uint8_t *) ref_pdu + offset;
	src.length = len;
	src.type = TAG_SEQUENCE;
	src.flags = FLAG_STRUCTURED;

	SnmpPDU pdu;
	int res1 = decode_snmp_pdu(&src, &pdu);
	if (res1) {
		fprintf(stderr, "Failed to decode %s : result code %i\n", name, res1);
		exit(1);
	}

	src.value = pdu.scoped_pdu.encrypted.data + scoped_offset;
	src.length = pdu.scoped_pdu.encrypted.len - scoped_offset;
	src.type = TAG_SEQUENCE;
	src.flags = FLAG_STRUCTURED;

	int res2 = decode_snmp_scoped_pdu(&src, &pdu.scoped_pdu.decrypted);
	if (res2) {
		fprintf(stderr, "Failed to decode %s scoped PDU : result code %i\n", name, res2);
		exit(1);
	}

#ifdef DEBUG
	dump_snmp_pdu(&pdu, 1);
#endif

	uint8_t enc_buf[ref_pdu_len];
	buf_t buf;
	init_obuf(&buf, enc_buf, ref_pdu_len);

	int res3 = encode_snmp_scoped_pdu(&pdu.scoped_pdu.decrypted, &buf);
	if (res3) {
		fprintf(stderr, "Failed to encode %s scoped PDU : result code %i\n", name, res3);
		exit(1);
	}

	int res4 = encode_snmp_pdu(&pdu, &buf, buf.size - buf.pos);
	if (res4) {
		fprintf(stderr, "Failed to encode %s PDU : result code %i\n", name, res4);
		exit(1);
	}

	if (memcmp(enc_buf, ref_pdu, ref_pdu_len)) {
		fprintf(stderr, "Failed to encode %s PDU : illegal output\n", name);
		exit(1);
	}
}

static void test_private_pdu(const uint8_t *ref_pdu, size_t ref_pdu_len, int offset, size_t len,
		int scoped_offset, char *name, int time_sync, char *auth_pass, char *priv_pass,
		const uint8_t *engine_id, size_t engine_id_len, SnmpSecurityLevel level, int valid)
{
	SnmpUSMContext context;
	context.level = level;
	memcpy(context.user_name, "management", sizeof("management"));
	context.user_name_len = sizeof("management");
	context.get_engine_boots = get_engine_boots;
	context.get_engine_time = get_engine_time;

	SnmpUSMSecret priv_secret;
    priv_secret.is_key = 0;
    priv_secret.secret.password = priv_pass;
    priv_secret.secret_len = priv_pass == NULL ? 0 : strlen(priv_pass);
    SnmpUSMSecret auth_secret;
    auth_secret.is_key = 0;
    auth_secret.secret.password = auth_pass;
    auth_secret.secret_len = auth_pass == NULL ? 0 : strlen(auth_pass);

	if (derive_usm_master_keys(&priv_secret, &auth_secret, &context)
			|| derive_usm_diversified_keys(engine_id, engine_id_len, &context)) {
		fprintf(stderr, "Failed to decode %s : initializing keyset failed.\n", name);
		exit(1);
	}

	uint8_t *pdu_copy = malloc(sizeof(uint8_t) * ref_pdu_len);
	if (pdu_copy == NULL) {
		exit(1);
	}
	memcpy(pdu_copy, ref_pdu, ref_pdu_len);

	asn1raw_t src;
	src.value = (uint8_t *) pdu_copy + offset;
	src.length = len;
	src.type = TAG_SEQUENCE;
	src.flags = FLAG_STRUCTURED;

	SnmpPDU pdu;
	int res1 = decode_snmp_pdu(&src, &pdu);
	if (res1) {
		fprintf(stderr, "Failed to decode %s : result code %i\n", name, res1);
		exit(1);
	}

	int res2 = process_incoming_pdu(pdu_copy, ref_pdu_len, &pdu, &context, time_sync);
	if ((res2 != 0) ^ !valid) {
		fprintf(stderr, "Failed to process PDU %s : result code %i\n", name, res2);
		exit(1);
	}

	if (!valid) {
		return;
	}

#ifdef DEBUG
	dump_snmp_pdu(&pdu, 1);
#endif

	uint8_t enc_buf[ref_pdu_len];
	buf_t buf;
	init_obuf(&buf, enc_buf, ref_pdu_len);

	int res3 = process_outgoing_pdu(&pdu, &buf, &context);
	if (res3) {
		fprintf(stderr, "Failed to encode %s PDU : result code %i\n", name, res3);
		exit(1);
	}

	if (level == AUTH_PRIV) {
		SnmpPDU pdu2;
		asn1raw_t src2;
		src2.value = (uint8_t *) &buf.buffer[buf.pos] + offset;
		src2.length = len;
		src2.type = TAG_SEQUENCE;
		src2.flags = FLAG_STRUCTURED;

		int res4 = decode_snmp_pdu(&src2, &pdu2);
		if (res4) {
			fprintf(stderr, "Failed to decode encoded %s : result code %i\n", name, res4);
			exit(1);
		}

		int res5 = process_incoming_pdu(&buf.buffer[buf.pos],
		    buf.size - buf.pos, &pdu2, &context, time_sync);
		if (res5 != 0) {
			fprintf(stderr, "Failed to process encoded PDU %s : result code %i\n", name, res5);
			exit(1);
		}

		if (pdu2.security_params.auth_engine_boots != get_engine_boots()) {
			fprintf(stderr, "Encoded PDU has invalid engine boot counter\n");
			exit(1);
		} else if (pdu2.security_params.auth_engine_time != get_engine_time()) {
			fprintf(stderr, "Encoded PDU has invalid engine timestamp\n");
			exit(1);
		}
	} else {
		if (memcmp(enc_buf, ref_pdu, ref_pdu_len)) {
			fprintf(stderr, "Failed to encode %s PDU : illegal output\n", name);
			exit(1);
		}
	}
}

void test_derive_session_key()
{
	SnmpUSMContext context;
	uint8_t engine_id[] = { 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02 };
	uint8_t master_expected[] = { 0x9f, 0xb5, 0xcc, 0x03, 0x81, 0x49, 0x7b, 0x37, 0x93, 0x52, 0x89, 0x39, 0xff, 0x78, 0x8d, 0x5d, 0x79, 0x14, 0x52, 0x11 };
	uint8_t diversified_key[] = { 0x66, 0x95, 0xfe, 0xbc, 0x92, 0x88, 0xe3, 0x62, 0x82, 0x23, 0x5f, 0xc7, 0x15, 0x1f, 0x12, 0x84, 0x97, 0xb3, 0x8f, 0x3f };

	SnmpUSMSecret secret;
	secret.is_key = 0;
	secret.secret.password = "maplesyrup";
	secret.secret_len = strlen(secret.secret.password);

	int res1 = derive_usm_master_keys(NULL, &secret, &context);
	if (res1) {
		fprintf(stderr, "Failed to derive master key : result code %i\n", res1);
		exit(1);
	} else if (memcmp(context.auth_key, master_expected, sizeof(master_expected))) {
		fprintf(stderr, "Failed to derive master key : result invalid\n");
		exit(1);
	}

	int res2 = derive_usm_diversified_keys(engine_id, sizeof(engine_id), &context);
	if (res2) {
		fprintf(stderr, "Failed to derive diversified key : result code %i\n", res2);
		exit(1);
	} else if (memcmp(diversified_key, context.auth_key, sizeof(diversified_key))) {
		fprintf(stderr, "Failed to derive diversified key : result invalid\n");
		exit(1);
	}
}

